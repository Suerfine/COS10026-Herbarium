			<dl>
				<dt>Family</dt>
				<dd>
					This is the highest classification group normally referred to. Each member of a plant family shares many botanical features.
					Modern classification assigns a type of plant to each family as an example of that family’s characteristics as distinguishable from other families. The names of families end in "aceae."
				</dd>
				<dt>Genus</dt>
				<dd>
					This is the part of plant nomenclature that is the most familiar. For example, Papaver is the genus for Poppy. Plants in a genus are
					easily recognizable as belonging to the same group. The name of the genus should always be capitalized. Example: Red Poppy is Papaver rhoeas.
				</dd>
				<dt>Species</dt>
				<dd>
					This is the level of classification that defines the individual plant. Here some aspects of the plant are more specifically defined — color, leaf shape,
					or place where or by whom it was discovered. The use of the genus and species names together always refer to only one plant.
				</dd>
			</dl>
