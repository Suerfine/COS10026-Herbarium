		<h1 class="classify-h1">Species</h1>
		
		<p class="classify-p">
		The leaves of some species in the Lauraceae have domatia in the axils of their veins. The domatia are home to certain mites. Other lauraceous species,
		members of the genus Pleurothyrium in particular, have a symbiotic relationship with ants 
		that protect and defend the tree. Some Ocotea species are also used as nesting sites by ants, which may live in leaf pockets 
		or in hollowed-out stems Defense mechanisms that occur among members of the Lauraceae include irritant or toxic sap or tissues that repel or poison many herbivorous organisms.</p>
		
		
		
		
