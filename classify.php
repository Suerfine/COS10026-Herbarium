		<p class="classify-p">
		Lauraceae, or the laurels, is a plant family that includes the true laurel and its closest relatives.
		This family comprises about 2850 known species in about 45 genera worldwide.They are dicotyledons,
		and occur mainly in warm temperate and tropical regions, especially Southeast Asia and South America.
		Many are aromatic evergreen trees or shrubs, but some, such as Sassafras, are deciduous,
		or include both deciduous and evergreen trees and shrubs, especially in tropical and temperate climates.
		The genus Cassytha is unique in the Lauraceae in that its members are parasitic vines. Most laurels are highly poisonous.</p>
		
		<h2 class="classify-h2">Genera</h2>
		<p class="classify-p">
			Because the family is so ancient and was so widely distributed on the Gondwana supercontinent,
			modern species commonly occur in relict populations isolated by geographical barriers, for instance on islands or tropical mountains.
			Relict forests retain endemic fauna and flora in communities of great value in inferring 
			the palaeontological succession and climate change that followed the breakups of the supercontinents..
		</p>
		
		
