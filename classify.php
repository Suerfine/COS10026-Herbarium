		<section class="classify-section-definition">
			<h2>Plant taxonomy is the science that finds, identifies, describes, classifies, and names plants. Plants can be classified into:</h2>
			<figure>
				<img src="images/classify/chart.jpg">
				<figcaption>Plant classification of the Fagaceae family</figcaption>
